const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function emptyDir(dirPath) {
  fs.rmSync(dirPath, { recursive: true, force: true });
  ensureDir(dirPath);
}

function normalizeRelative(relativePath) {
  return String(relativePath || '').replace(/\\/g, '/').replace(/^\/+/, '');
}

function copyFile(relativeSource, relativeTarget, options) {
  const sourcePath = path.join(REPO_ROOT, normalizeRelative(relativeSource));
  const targetPath = path.join(REPO_ROOT, normalizeRelative(relativeTarget));
  if (!fs.existsSync(sourcePath)) {
    if (options && options.optional) {
      return false;
    }
    throw new Error('Missing source file: ' + sourcePath);
  }

  ensureDir(path.dirname(targetPath));
  fs.copyFileSync(sourcePath, targetPath);
  return true;
}

function copyDirectory(relativeSource, relativeTarget, options) {
  const sourcePath = path.join(REPO_ROOT, normalizeRelative(relativeSource));
  const targetPath = path.join(REPO_ROOT, normalizeRelative(relativeTarget));
  if (!fs.existsSync(sourcePath)) {
    if (options && options.optional) {
      return false;
    }
    throw new Error('Missing source directory: ' + sourcePath);
  }

  const filter = options && typeof options.filter === 'function'
    ? options.filter
    : function () { return true; };

  walkDirectory(sourcePath, function (absolutePath, stats) {
    const relativeFromSource = path.relative(sourcePath, absolutePath);
    if (!relativeFromSource) {
      return;
    }

    if (!filter(absolutePath, relativeFromSource, stats)) {
      return;
    }

    const destinationPath = path.join(targetPath, relativeFromSource);
    if (stats.isDirectory()) {
      ensureDir(destinationPath);
      return;
    }

    ensureDir(path.dirname(destinationPath));
    fs.copyFileSync(absolutePath, destinationPath);
  });

  return true;
}

function walkDirectory(rootPath, visitor) {
  const stats = fs.statSync(rootPath);
  visitor(rootPath, stats);
  if (!stats.isDirectory()) {
    return;
  }

  fs.readdirSync(rootPath, { withFileTypes: true }).forEach(function (entry) {
    const absolutePath = path.join(rootPath, entry.name);
    walkDirectory(absolutePath, visitor);
  });
}

function writeRepoFile(relativeTarget, content) {
  const targetPath = path.join(REPO_ROOT, normalizeRelative(relativeTarget));
  ensureDir(path.dirname(targetPath));
  fs.writeFileSync(targetPath, content, 'utf8');
}

function readRepoFile(relativePath) {
  return fs.readFileSync(path.join(REPO_ROOT, normalizeRelative(relativePath)), 'utf8');
}

function resolveLocalModule(specifier, importerPath) {
  const importerDir = path.posix.dirname(importerPath);
  const joined = path.posix.normalize(path.posix.join(importerDir, specifier));
  if (path.posix.extname(joined)) {
    return joined;
  }
  return joined + '.js';
}

function collectModuleGraph(entryRelativePath, graph, seen) {
  const normalizedEntry = '/' + normalizeRelative(entryRelativePath);
  if (seen.has(normalizedEntry)) {
    return;
  }
  seen.add(normalizedEntry);

  const absolutePath = path.join(REPO_ROOT, normalizedEntry.slice(1));
  const source = fs.readFileSync(absolutePath, 'utf8');
  graph[normalizedEntry] = source;

  const requirePattern = /require\((['"])(\.{1,2}\/[^'"]+)\1\)/g;
  var match;
  while ((match = requirePattern.exec(source))) {
    collectModuleGraph(resolveLocalModule(match[2], normalizedEntry), graph, seen);
  }
}

function buildCommonJsWorkerBundle(options) {
  const source = options && typeof options === 'object' ? options : {};
  const entryPath = normalizeRelative(source.entry);
  const graph = {};
  collectModuleGraph(entryPath, graph, new Set());
  const moduleEntries = Object.keys(graph)
    .sort()
    .map(function (moduleId) {
      return JSON.stringify(moduleId) + ': function (module, exports, require, __filename, __dirname) {\n' + graph[moduleId] + '\n}';
    })
    .join(',\n');

  const namedExports = Array.isArray(source.namedExports) ? source.namedExports : [];
  const exportLines = namedExports.map(function (exportName) {
    return 'export const ' + exportName + ' = __entry[' + JSON.stringify(exportName) + '];';
  }).join('\n');

  return [
    '// Generated by scripts/build-cloudflare-utils.js',
    'const __modules = {' + (moduleEntries ? '\n' + moduleEntries + '\n' : '') + '};',
    'const __cache = Object.create(null);',
    'function __dirnameFromId(moduleId) {',
    '  const normalized = String(moduleId || "");',
    '  const boundary = normalized.lastIndexOf("/");',
    '  return boundary <= 0 ? "/" : normalized.slice(0, boundary);',
    '}',
    'function __resolve(moduleId, specifier) {',
    '  if (typeof specifier !== "string" || !specifier) {',
    '    return specifier;',
    '  }',
    '  if (specifier[0] !== ".") {',
    '    return specifier;',
    '  }',
    '  const base = __dirnameFromId(moduleId).split("/").filter(Boolean);',
    '  specifier.split("/").forEach(function (segment) {',
    '    if (!segment || segment === ".") return;',
    '    if (segment === "..") {',
    '      if (base.length) base.pop();',
    '      return;',
    '    }',
    '    base.push(segment);',
    '  });',
    '  let resolved = "/" + base.join("/");',
    '  if (!/\.[a-z]+$/i.test(resolved)) {',
    '    resolved += ".js";',
    '  }',
    '  return resolved;',
    '}',
    'function __require(specifier, parentId) {',
    '  const resolvedId = parentId ? __resolve(parentId, specifier) : specifier;',
    '  if (!Object.prototype.hasOwnProperty.call(__modules, resolvedId)) {',
    '    if (typeof require === "function") {',
    '      return require(resolvedId);',
    '    }',
    '    throw new Error("Unsupported external module in Cloudflare bundle: " + resolvedId);',
    '  }',
    '  if (__cache[resolvedId]) {',
    '    return __cache[resolvedId].exports;',
    '  }',
    '  const module = { exports: {} };',
    '  __cache[resolvedId] = module;',
    '  __modules[resolvedId](module, module.exports, function (nextSpecifier) { return __require(nextSpecifier, resolvedId); }, resolvedId, __dirnameFromId(resolvedId));',
    '  return module.exports;',
    '}',
    'const __entry = __require("/' + entryPath + '");',
    'const __defaultExport = (__entry && Object.prototype.hasOwnProperty.call(__entry, "default")) ? __entry.default : __entry;',
    'export default __defaultExport;',
    exportLines,
    '',
  ].filter(Boolean).join('\n');
}

module.exports = {
  REPO_ROOT,
  ensureDir,
  emptyDir,
  copyFile,
  copyDirectory,
  writeRepoFile,
  readRepoFile,
  buildCommonJsWorkerBundle,
};
