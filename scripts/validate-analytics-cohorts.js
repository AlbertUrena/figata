function main() {
  const pipelineResult = analyticsPipeline.buildAnalyticsPipeline(buildFixtureEvents(), {
    sourceLabel: 'cohort-fixture',
    processedAt: '2026-04-20T21:00:00.000Z',
  });
  const snapshot = analyticsCohorts.buildCohortSnapshot(pipelineResult, {
    generatedAt: '2026-04-20T21:00:00.000Z',
    scopeMode: 'business_only',
  });

  assert(snapshot.cohorts.by_entry_source.length >= 2, 'Expected multiple entry-source cohort rows');
  assert(snapshot.retention.overall.return_1d_rate === 0.5, 'Expected 1-day return rate of 0.5');
  assert(snapshot.retention.overall.return_30d_rate === 1, 'Expected 30-day return rate of 1');
  assert(snapshot.timing.by_hour.length >= 2, 'Expected hour timing rows');
  assert(snapshot.performance_context.by_network_type.some((row) => row.label === '3g'), 'Expected 3g network row');
  assert(snapshot.curiosity_items.length > 0, 'Expected curiosity items');
  assert(snapshot.curiosity_items[0].item_id === 'pizza-curiosa', 'Expected curiosity item to rank first');

  console.log('Analytics cohorts snapshot valid.');
  console.log(`Visitor-type cohorts: ${snapshot.cohorts.by_visitor_type.length}`);
  console.log(`Timing hours: ${snapshot.timing.by_hour.length}`);
}

try {
  main();
} catch (error) {
  console.error(error && error.message ? error.message : error);
  process.exit(1);
}
